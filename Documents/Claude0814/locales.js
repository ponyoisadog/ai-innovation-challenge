// Language switcher
let currentLang = 'en';

function switchLanguage(lang) {
  currentLang = lang;
  updatePageLanguage();
  localStorage.setItem('preferredLanguage', lang);
}

function updatePageLanguage() {
  const elements = document.querySelectorAll('[data-i18n]');
  elements.forEach(element => {
    const key = element.getAttribute('data-i18n');
    if (locales[currentLang][key]) {
      element.textContent = locales[currentLang][key];
    }
  });
  
  // Update placeholders
  const inputs = document.querySelectorAll('input[data-i18n-placeholder], textarea[data-i18n-placeholder]');
  inputs.forEach(input => {
    const key = input.getAttribute('data-i18n-placeholder');
    if (locales[currentLang][key]) {
      input.placeholder = locales[currentLang][key];
    }
  });
  
  // Update select options
  const selects = document.querySelectorAll('select[data-i18n-options]');
  selects.forEach(select => {
    const key = select.getAttribute('data-i18n-options');
    if (locales[currentLang][key]) {
      select.innerHTML = `<option value="">${locales[currentLang][key]}</option>`;
      // Add category options
      const categories = ['healthcare', 'education', 'environment', 'productivity', 'creative', 'other'];
      categories.forEach(cat => {
        const option = document.createElement('option');
        option.value = cat;
        option.textContent = locales[currentLang][cat];
        select.appendChild(option);
      });
    }
  });
}

// Initialize language
document.addEventListener('DOMContentLoaded', () => {
  const savedLang = localStorage.getItem('preferredLanguage') || 'en';
  switchLanguage(savedLang);
});
